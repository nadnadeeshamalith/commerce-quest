function shuffleArray(array) {
  let newArr = [...array];
  for (let i = newArr.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [newArr[i], newArr[j]] = [newArr[j], newArr[i]];
  }
  return newArr;
}

function generateMathGen() {
  const operations = ['+', '-', '*'];
  const op = operations[Math.floor(Math.random() * operations.length)];
  let q, a, w1, w2, w3, exp;
  let num1 = Math.floor(Math.random() * 50) + 10;
  let num2 = Math.floor(Math.random() * 20) + 1;
  
  if (op === '+') {
    q = `${num1} සහ ${num2} හි එකතුව කීයද?`;
    a = num1 + num2;
    exp = `${num1} + ${num2} = ${a}`;
  } else if (op === '-') {
    if (num1 < num2) { let temp = num1; num1 = num2; num2 = temp; }
    q = `${num1} න් ${num2} ක් අඩු කළ විට පිළිතුර කුමක්ද?`;
    a = num1 - num2;
    exp = `${num1} - ${num2} = ${a}`;
  } else if (op === '*') {
    num1 = Math.floor(Math.random() * 12) + 2;
    num2 = Math.floor(Math.random() * 9) + 2;
    q = `${num1} වරක් ${num2} කීයද?`;
    a = num1 * num2;
    exp = `${num1} x ${num2} = ${a}`;
  }
  
  w1 = a + Math.floor(Math.random() * 5) + 1;
  w2 = a - Math.floor(Math.random() * 4) - 1;
  w3 = a + Math.floor(Math.random() * 10) + 5;
  if (w2 < 0) w2 = a + 2;
  
  return { q, a: String(a), w: [String(w1), String(w2), String(w3)], exp };
}

function generateGrammar() {
  const types = ['opposite', 'plural', 'math', 'animal', 'gk', 'nature'];
  const type = types[Math.floor(Math.random() * types.length)];
  
  let item = null;
  let retries = 0;
  while (!item && retries < 10) {
    if (type === 'opposite') {
      const pool = shuffleArray(opposites);
      item = { ...pool[0], exp: `'${pool[0].q}' යන්නෙහි විරුද්ධ පදය '${pool[0].a}' වේ.` };
      item.q = `'${item.q}' යන්නෙහි විරුද්ධ පදය කුමක්ද?`;
    } else if (type === 'plural') {
      const pool = shuffleArray(plurals);
      item = { ...pool[0], exp: `'${pool[0].q}' යන්නෙහි බහු වචනය '${pool[0].a}' වේ.` };
      item.q = `'${item.q}' යන්නෙහි බහු වචනය කුමක්ද?`;
    } else if (type === 'math') {
      item = generateMathGen();
    } else if (type === 'animal') {
      item = shuffleArray(animals)[0];
      item.exp = `නිවැරදි පිළිතුර: ${item.a}`;
    } else if (type === 'gk') {
      item = shuffleArray(generalKnowledge)[0];
      item.exp = `නිවැරදි පිළිතුර: ${item.a}`;
    } else if (type === 'nature') {
      item = shuffleArray(nature)[0];
      item.exp = `නිවැරදි පිළිතුර: ${item.a}`;
    }
    
    // Check if unique question text
    if (usedQuestions.has(item.q)) {
      item = null;
      retries++;
    }
  }
  
  if (!item) {
     item = generateMathGen(); // Fallback to infinite math
     while (usedQuestions.has(item.q)) {
       item = generateMathGen();
     }
  }
  
  usedQuestions.add(item.q);
  return item;
}

