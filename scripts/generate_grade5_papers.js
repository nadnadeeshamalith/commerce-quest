const seenQuestions = new Set();
function addQuestion(pool, qText, options, answerText) {
    if (seenQuestions.has(qText)) return false;
    seenQuestions.add(qText);
    
    let shuffledOptions = [...options];
    let ansIndex = shuffledOptions.indexOf(answerText);
    if (ansIndex === -1) {
        shuffledOptions[0] = answerText; // safe fallback
    }
    shuffledOptions = shuffle(shuffledOptions);
    let finalAnswerIndex = shuffledOptions.indexOf(answerText);
    
    pool.push({
        question: qText,
        options: shuffledOptions,
        answer: finalAnswerIndex
    });
    return true;
}

