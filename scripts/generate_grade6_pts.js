function shuffle(array) {
    let currentIndex = array.length, randomIndex;
    while (currentIndex !== 0) {
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex--;
        [array[currentIndex], array[randomIndex]] = [array[randomIndex], array[currentIndex]];
    }
    return array;
}

const ptsPool = [];

function addQuestion(qText, options, answerText) {
    let shuffledOptions = [...options];
    let ansIndex = shuffledOptions.indexOf(answerText);
    if (ansIndex === -1) {
        shuffledOptions[0] = answerText;
    }
    shuffledOptions = shuffle(shuffledOptions);
    let finalAnswerIndex = shuffledOptions.indexOf(answerText);
    
    ptsPool.push({
        question: qText,
        options: shuffledOptions,
        answer: finalAnswerIndex
    });
}

